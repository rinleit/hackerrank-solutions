def pickingNumbers(a):
    counts = [0] * (max(a) + 1)
    for i in a:
        counts[i] += 1
        if i - 1 in a:
            counts[i - 1] += 1
    return max(counts)



if __name__ == '__main__':
    fptr = open(os.environ['OUTPUT_PATH'], 'w')

    n = int(input().strip())

    a = list(map(int, input().rstrip().split()))

    result = pickingNumbers(a)

    fptr.write(str(result) + '\n')

